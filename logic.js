  let like = e.target
  Hoteles.forEach(element =>{
    if(element.corazon === "activo"){
      like.className= "fa-regular fa-heart"
      element.corazon="desactivo"
    }else{

      like.className= "fa-regular fa-heart likeCheck"
      element.corazon="activo"

    }
  })
  
